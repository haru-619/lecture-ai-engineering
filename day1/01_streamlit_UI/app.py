import streamlit as st
import pandas as pd
import numpy as np
import time

# ============================================
# ページ設定
# ============================================
st.set_page_config(  # ★コメント解除＋設定変更
    page_title="自己紹介フォーム",
    layout="centered",
    initial_sidebar_state="auto"
)

# ============================================
# タイトルと説明
# ============================================
st.title("🎉 自己紹介フォーム")
st.markdown("以下のフォームに入力して、あなたのプロフィールを表示しましょう。")

# ============================================
# サイドバー 
# ============================================
st.sidebar.header("入力オプション")
hobby = st.sidebar.selectbox("趣味を選んでください", ["映画鑑賞", "読書", "運動", "ゲーム", "旅行"])

# ============================================
# 入力フォーム
# ============================================
st.header("あなたの基本情報")

name = st.text_input("あなたの名前", "")
age = st.slider("年齢", 10, 100, 25)
language = st.selectbox("好きなプログラミング言語は?", ["Python", "JavaScript", "C#", "Rust", "Go"])

# ボタンを押すと結果表示
if st.button("プロフィールを表示"):  # ★コメント解除＆実用化
    st.success("以下があなたのプロフィールです👇")
    st.markdown(f"""
    - **名前**: {name if name else '未入力'}
    - **年齢**: {age}歳
    - **趣味**: {hobby}
    - **好きな言語**: {language}
    """)
    st.balloons()  # ★見た目強化

# ============================================
# フッター
# ============================================
st.divider()
st.caption("© 2025 Streamlit UI 改良デモ")
